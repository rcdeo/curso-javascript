/**
 * VARIAVEIS, TYPE/INSTANCE & PARSE/TO
 *
 * var: Declara uma variável com escopo global ou de função, permite reatribuição e redeclaração (evitar).
 * let: Declara uma variável com escopo de bloco, permite reatribuição mas não redeclaração (moderna e preferida).
 * const: Declara uma constante imutável (valor não pode mudar), deve ser inicializada na declaracao (para segurança).
 *
 * typeof: Operador unário que retorna uma string indicando o tipo primitivo de um valor (e.g., "string", "number", "boolean").
 * instanceof: Operador binário que checa se um objeto e uma instância de uma classe específica (verifica a cadeia de prototipos).
 *
 * parseInt (converte para inteiro)
 * parseFloat (converte para um número de ponto flutuante)
 * toString (converte para texto)
 */

